/*Create a C++ program that demonstrates the use of different C++ data types 
(int, float, double, char, bool). Declare variables for each type, 
assign them values, and output their values to the console.*/
#include <iostream>

int main() 
{
    int intvar = 10;
    float floatvar = 3.14f;
    double doublevar = 2.71828;
    char charvar = 'A';
    bool boolvar = true;

    std::cout << intvar << std::endl;
    std::cout << floatvar << std::endl;
    std::cout << doublevar << std::endl;
    std::cout << charvar << std::endl;
    std::cout << boolvar << std::endl;
}
